/// <summary>
/// The user address class
/// </summary>
public class UserAddress
{
    /// <summary>
    /// Gets or sets the value of the region
    /// </summary>
    public string Region { get; set; }

    /// <summary>
    /// Gets or sets the value of the town
    /// </summary>
    public string Town { get; set; }

    /// <summary>
    /// Gets or sets the value of the address
    /// </summary>
    public string Address { get; set; }

    /// <summary>
    /// Gets or sets the value of the postcode
    /// </summary>
    public string Postcode { get; set; }
}